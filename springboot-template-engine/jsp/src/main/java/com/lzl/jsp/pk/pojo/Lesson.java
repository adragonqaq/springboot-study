package com.lzl.jsp.pk.pojo;

public class Lesson {
   private int id;//
   private String lessonname;//课程名称
   private Float xf;//学分
   private int ks;//课时
   private String category;//课程类型
   private int start;//开始周
   
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getLessonname() {
	return lessonname;
}
public void setLessonname(String lessonname) {
	this.lessonname = lessonname;
}
public Float getXf() {
	return xf;
}
public void setXf(Float xf) {
	this.xf = xf;
}
public int getKs() {
	return ks;
}
public void setKs(int ks) {
	this.ks = ks;
}
public String getCategory() {
	return category;
}
public void setCategory(String category) {
	this.category = category;
}
public int getStart() {
	return start;
}
public void setStart(int start) {
	this.start = start;
}

   
   
}
