package com.lzl.jsp.pk.pojo;
public class ClassRoom {
	private int id;
	private String roomname;//教室名
	private int accptnum;//教室容纳人数
	private String category;//教室类别
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRoomname() {
		return roomname;
	}
	public void setRoomname(String roomname) {
		this.roomname = roomname;
	}
	public int getAccptnum() {
		return accptnum;
	}
	public void setAccptnum(int accptnum) {
		this.accptnum = accptnum;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	
	
}
